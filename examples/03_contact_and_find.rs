use m3_9_displacement::v1::FileName;
use m3_9_displacement::set_env;

use polars::prelude::*;

use anyhow::Result;


/*
複数ファイルを結合したいデータフレームの作成
FileName::ConcatenatedRowをライブラリに追加
*/
fn main() -> Result<()>{
    set_env();
    let lf = FileName::ConcatenatedRow.read_file()?;

    
    let result_df = lf
        .with_column(
            (col("date").cast(DataType::String) + lit(" ") + col("time").cast(DataType::String))
                .str().to_datetime(None,None,StrptimeOptions::default(), lit(""))
                .alias("datetime")
        )
        .with_column(
            (col("datetime")-col("datetime").shift(lit(1))).alias("stop_time")
        )
        .filter(col("interval").gt_eq(lit(800000)))
        .with_column(
            (col("datetime").shift(lit(-1))-col("datetime")).alias("working_time")
        )
        .collect()?;


    println!("{}", result_df);

    Ok(())
}

